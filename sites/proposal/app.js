// Gas Repricing Proposal — markdown report viewer
(function () {
    'use strict';

    var REPORTS = {
        proposal: { file: 'new_gas_proposal.md', label: 'Proposal', stripImages: false },
        runtime:  { file: 'runtime_estimation_autogenerated_report.md', label: 'Runtime Estimation', stripImages: true },
        glue:     { file: 'glue_opcodes_autogenerated_report.md', label: 'Glue Opcodes', stripImages: false }
    };

    var CSV_FILES = [
        'new_gas.csv',
        'results.csv',
        'glue_results.csv',
        'gas_bench_data.csv',
        'glue_opcodes_by_test.csv'
    ];

    var cache = {};
    var activeReport = 'proposal';

    // ── DOM refs ──────────────────────────────────────────────────────────────
    var loadingEl = document.getElementById('loading');
    var errorEl   = document.getElementById('error');
    var contentEl = document.getElementById('content');

    // ── State helpers ─────────────────────────────────────────────────────────
    function showLoading() {
        loadingEl.classList.remove('hidden');
        errorEl.classList.add('hidden');
        contentEl.classList.add('hidden');
    }

    function showError(msg) {
        loadingEl.classList.add('hidden');
        errorEl.textContent = msg;
        errorEl.classList.remove('hidden');
        contentEl.classList.add('hidden');
    }

    function showContent(html) {
        loadingEl.classList.add('hidden');
        errorEl.classList.add('hidden');
        contentEl.innerHTML = html;
        contentEl.classList.remove('hidden');
    }

    // ── Markdown processing ───────────────────────────────────────────────────
    function stripImgTags(md) {
        // Remove <img> HTML tags (used in runtime estimation report for per-opcode plots)
        return md.replace(/^<img\s[^>]*\/?\s*>\s*$/gm, '');
    }

    function loadReport(name) {
        var report = REPORTS[name];
        if (!report) return;

        activeReport = name;
        updateTabs();

        if (cache[name]) {
            showContent(cache[name]);
            window.scrollTo(0, 0);
            return;
        }

        showLoading();

        fetch(report.file)
            .then(function (res) {
                if (!res.ok) throw new Error('Report not available yet (HTTP ' + res.status + ')');
                return res.text();
            })
            .then(function (md) {
                if (report.stripImages) {
                    md = stripImgTags(md);
                }
                var html = marked.parse(md);
                cache[name] = html;
                showContent(html);
                window.scrollTo(0, 0);
            })
            .catch(function (err) {
                showError(err.message || 'Failed to load report.');
            });
    }

    // ── Tab navigation ────────────────────────────────────────────────────────
    function updateTabs() {
        var tabs = document.querySelectorAll('.report-tab');
        for (var i = 0; i < tabs.length; i++) {
            if (tabs[i].getAttribute('data-report') === activeReport) {
                tabs[i].classList.add('active');
            } else {
                tabs[i].classList.remove('active');
            }
        }
    }

    function initTabs() {
        var tabs = document.querySelectorAll('.report-tab');
        for (var i = 0; i < tabs.length; i++) {
            tabs[i].addEventListener('click', function () {
                loadReport(this.getAttribute('data-report'));
            });
        }
    }

    // ── CSV download links ────────────────────────────────────────────────────
    function initDownloads() {
        var container = document.getElementById('csv-downloads');
        for (var i = 0; i < CSV_FILES.length; i++) {
            var a = document.createElement('a');
            a.href = CSV_FILES[i];
            a.className = 'csv-link';
            a.download = '';
            a.textContent = CSV_FILES[i];
            container.appendChild(a);
        }
    }

    // ── Theme toggle ──────────────────────────────────────────────────────────
    (function () {
        var toggle = document.getElementById('theme-toggle');
        var current = document.documentElement.getAttribute('data-theme') || 'light';
        toggle.textContent = current === 'dark' ? 'Light mode' : 'Dark mode';
        toggle.addEventListener('click', function () {
            var cur = document.documentElement.getAttribute('data-theme');
            var next = cur === 'dark' ? 'light' : 'dark';
            document.documentElement.setAttribute('data-theme', next);
            document.documentElement.style.colorScheme = next;
            localStorage.setItem('theme', next);
            toggle.textContent = next === 'dark' ? 'Light mode' : 'Dark mode';
        });
    })();

    // ── Init ──────────────────────────────────────────────────────────────────
    if (typeof marked === 'undefined') {
        showError('Failed to load markdown renderer.');
    } else {
        initTabs();
        initDownloads();
        loadReport('proposal');
    }
})();
