// Gas Repricing Proposal — markdown report viewer
(function () {
    'use strict';

    var REPORTS = {
        proposal: { file: 'new_gas_proposal.md', label: 'Proposal', stripImages: false },
        runtime:  { file: 'runtime_estimation_autogenerated_report.md', label: 'Runtime Estimation', stripImages: false },
        glue:     { file: 'glue_opcodes_autogenerated_report.md', label: 'Glue Opcodes', stripImages: false }
    };

    var CSV_FILES = [
        'new_gas.csv',
        'results.csv',
        'glue_results.csv',
        'gas_bench_data.csv',
        'glue_opcodes_by_test.csv'
    ];

    var cache = {};
    var activeReport = 'proposal';

    // ── DOM refs ──────────────────────────────────────────────────────────────
    var loadingEl = document.getElementById('loading');
    var errorEl   = document.getElementById('error');
    var contentEl = document.getElementById('content');

    // ── State helpers ─────────────────────────────────────────────────────────
    function showLoading() {
        loadingEl.classList.remove('hidden');
        errorEl.classList.add('hidden');
        contentEl.classList.add('hidden');
    }

    function showError(msg) {
        loadingEl.classList.add('hidden');
        errorEl.textContent = msg;
        errorEl.classList.remove('hidden');
        contentEl.classList.add('hidden');
    }

    function showContent(html, skipScrollTop) {
        loadingEl.classList.add('hidden');
        errorEl.classList.add('hidden');
        // Add lazy loading to all images
        html = html.replace(/<img /g, '<img loading="lazy" ');
        contentEl.innerHTML = html;
        contentEl.classList.remove('hidden');

        // Make opcode sections collapsible in the runtime report
        if (activeReport === 'runtime') {
            makeOpcodeSectionsCollapsible(contentEl);
        }

        // Navigate to hash anchor if present
        if (!skipScrollTop) {
            scrollToHash();
        }
    }

    function makeOpcodeSectionsCollapsible(container) {
        var headings = container.querySelectorAll('h1');
        var skipTitles = [
            'Table of contents',
            'Operation run times estimation results - EIP-zkevm'
        ];

        for (var i = 0; i < headings.length; i++) {
            var h1 = headings[i];
            var title = h1.textContent.trim();

            // Skip non-opcode headings
            var skip = false;
            for (var j = 0; j < skipTitles.length; j++) {
                if (title.indexOf(skipTitles[j]) !== -1) { skip = true; break; }
            }
            if (skip) continue;

            var details = document.createElement('details');
            details.className = 'opcode-section';
            var summary = document.createElement('summary');
            summary.appendChild(h1.cloneNode(true));
            details.appendChild(summary);

            h1.parentNode.insertBefore(details, h1);
            h1.remove();

            // Move all siblings until next h1 into the details
            var next = details.nextSibling;
            while (next && !(next.nodeType === 1 && next.tagName === 'H1')) {
                var toMove = next;
                next = next.nextSibling;
                details.appendChild(toMove);
            }
        }
    }

    // ── Hash / anchor navigation ──────────────────────────────────────────────
    function scrollToHash() {
        var hash = window.location.hash;
        if (!hash) { window.scrollTo(0, 0); return; }

        var target = document.getElementById(hash.slice(1));
        if (!target) { window.scrollTo(0, 0); return; }

        // Open any parent <details> elements so the target is visible
        var el = target;
        while (el) {
            if (el.tagName === 'DETAILS') { el.open = true; }
            el = el.parentElement;
        }
        target.scrollIntoView();
    }

    window.addEventListener('hashchange', scrollToHash);

    // ── Markdown processing ───────────────────────────────────────────────────
    function stripImgTags(md) {
        // Remove <img> HTML tags (used in runtime estimation report for per-opcode plots)
        return md.replace(/^<img\s[^>]*\/?\s*>\s*$/gm, '');
    }

    function loadReport(name) {
        var report = REPORTS[name];
        if (!report) return;

        activeReport = name;
        updateTabs();

        if (cache[name]) {
            showContent(cache[name]);
            return;
        }

        showLoading();

        fetch(report.file)
            .then(function (res) {
                if (!res.ok) throw new Error('Report not available yet (HTTP ' + res.status + ')');
                return res.text();
            })
            .then(function (md) {
                if (report.stripImages) {
                    md = stripImgTags(md);
                }
                var html = marked.parse(md);
                cache[name] = html;
                showContent(html);
            })
            .catch(function (err) {
                showError(err.message || 'Failed to load report.');
            });
    }

    // ── Tab navigation ────────────────────────────────────────────────────────
    function updateTabs() {
        var tabs = document.querySelectorAll('.report-tab');
        for (var i = 0; i < tabs.length; i++) {
            if (tabs[i].getAttribute('data-report') === activeReport) {
                tabs[i].classList.add('active');
            } else {
                tabs[i].classList.remove('active');
            }
        }
    }

    function initTabs() {
        var tabs = document.querySelectorAll('.report-tab');
        for (var i = 0; i < tabs.length; i++) {
            tabs[i].addEventListener('click', function () {
                loadReport(this.getAttribute('data-report'));
            });
        }
    }

    // ── CSV download links ────────────────────────────────────────────────────
    function initDownloads() {
        var container = document.getElementById('csv-downloads');
        for (var i = 0; i < CSV_FILES.length; i++) {
            var a = document.createElement('a');
            a.href = CSV_FILES[i];
            a.className = 'csv-link';
            a.download = '';
            a.textContent = CSV_FILES[i];
            container.appendChild(a);
        }
    }

    // ── Theme toggle ──────────────────────────────────────────────────────────
    (function () {
        var toggle = document.getElementById('theme-toggle');
        var current = document.documentElement.getAttribute('data-theme') || 'light';
        toggle.textContent = current === 'dark' ? 'Light mode' : 'Dark mode';
        toggle.addEventListener('click', function () {
            var cur = document.documentElement.getAttribute('data-theme');
            var next = cur === 'dark' ? 'light' : 'dark';
            document.documentElement.setAttribute('data-theme', next);
            document.documentElement.style.colorScheme = next;
            localStorage.setItem('theme', next);
            toggle.textContent = next === 'dark' ? 'Light mode' : 'Dark mode';
        });
    })();

    // ── Init ──────────────────────────────────────────────────────────────────
    if (typeof marked === 'undefined') {
        showError('Failed to load markdown renderer.');
    } else {
        // Generate id attributes on headings so anchor links work
        marked.use({ renderer: {
            heading: function (args) {
                var text = args.text;
                var depth = args.depth;
                var id = text.toLowerCase().replace(/<[^>]+>/g, '').replace(/[^\w]+/g, '-').replace(/^-|-$/g, '');
                return '<h' + depth + ' id="' + id + '">' + text + '</h' + depth + '>\n';
            }
        }});
        initTabs();
        initDownloads();
        loadReport('proposal');
    }
})();
